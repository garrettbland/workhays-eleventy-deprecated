const htmlmin = require('html-minifier')
const lazyloadimages = require('@garrettbland/lazy-load-images')
const imagealttagcheck = require('@garrettbland/img-alt-tag-check')
let liquidJs = require('liquidjs')
const now = Date.now().toString()
const path = require('path')
const fs = require('fs-extra')

module.exports = (eleventyConfig) => {
    let options = {
        extname: '.liquid',
        dynamicPartials: true,
        root: 'src',
    }

    eleventyConfig.setLibrary('liquid', liquidJs(options))

    /**
     * Setup nice overlay so we are foreced to fix errors
     */
    eleventyConfig.setBrowserSyncConfig({
        ...eleventyConfig.browserSyncConfig,
    })

    /**
     * Watch styles generated by tailwind
     */
    eleventyConfig.addWatchTarget('dist/style.css')

    /**
     * Pass directories & files to top level of dist
     * Add Alpine JS CDN build directly
     */
    eleventyConfig.addPassthroughCopy({ public: '/' })
    eleventyConfig.addPassthroughCopy({
        './node_modules/alpinejs/dist/cdn.min.js': './alpine.js',
    })

    /**
     * Create 'lib' directory in serverless directory if
     * it doesn't exist
     */
    fs.mkdir('./serverless/lib', { recursive: true })

    /**
     * Copy example template into serverless directory
     */
    fs.copyFile(
        './src/pages/jobs/example.liquid',
        './serverless/example.liquid',
        (err) => {
            if (err) throw err
            console.log(
                `Copied over example.liquid to serverless dir...`
            )
        }
    )

    /**
     * Read all files in lib, and copy each file into serverless/lib
     */
    fs.readdir('./src/lib', (err, files) => {
        files.forEach((file) => {
            fs.copyFile(
                path.resolve(__dirname, 'src/lib/', file),
                `./serverless/lib/${file}`,
                (err) => {
                    if (err) throw err
                    console.log(`Copied ${file} to serverless dir...`)
                }
            )
        })
    })

    /**
     * Add version shortcode to version and cache bust our stylesheet and
     * javascript packages
     */
    eleventyConfig.addShortcode('version', function () {
        return now
    })

    /**
     * Validates that all images have an alt tag & value
     */
    eleventyConfig.addPlugin(imagealttagcheck)

    /**
     * Add native lazy loading to images
     */
    eleventyConfig.addPlugin(lazyloadimages)

    /**
     * Minify HTML
     */
    eleventyConfig.addTransform('htmlmin', (content, outputPath) => {
        if (outputPath.endsWith('.html')) {
            return htmlmin.minify(content, {
                useShortDoctype: true,
                removeComments: true,
                collapseWhitespace: true,
            })
        } else {
            return content
        }
    })

    return {
        passthroughFileCopy: true,
        dir: {
            input: 'src/pages',
            output: 'dist',
        },
    }
}
